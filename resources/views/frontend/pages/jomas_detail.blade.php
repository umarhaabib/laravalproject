		<h1 class="entry-title">Borrower Case Study: Financing the Jomas &#038; Beach Residence in 8 stages</h1>	</header><!-- .entry-header -->

	
	
		<div class="post-thumbnail">
			<img width="1200" height="675" src="{{URL::to('')}}/public/assets/frontend/blog_assets/wp-content/uploads/2020/05/Artboard-1-ENG-FINAL.jpg" class="attachment-post-thumbnail size-post-thumbnail wp-post-image jetpack-lazy-image" alt="Jomas &amp; Beach Residence" data-lazy-sizes="(max-width: 709px) 85vw, (max-width: 909px) 67vw, (max-width: 984px) 60vw, (max-width: 1362px) 62vw, 840px" data-lazy-src="https://blog.estateguru.co/wp-content/uploads/2020/05/Artboard-1-ENG-FINAL.jpg?is-pending-load=1" srcset="data:image/gif;base64,R0lGODlhAQABAIAAAAAAAP///yH5BAEAAAAALAAAAAABAAEAAAIBRAA7" />	</div><!-- .post-thumbnail -->

	
	<div class="entry-content">
		
<p><strong>The borrower: </strong>SIA Jomas 19, a developer with long-term experience in real estate, has executed several residential projects in different cities in Latvia, including Jurmala, in the past.</p>



<p><strong>The development:</strong> 18 new-build apartments located in the heart of Jurmala, 150 m from the most famous pedestrian street, Jomas, and 650 m from the sea.</p>



<span id="more-3365"></span>



<h2 class="has-text-color" style="color:#0071a1"><strong>Request:</strong></h2>



<p>A development loan of €650 000 in 8 stages for the construction and subsequent sale of the apartment building.</p>



<h2 class="has-text-color" style="color:#0071a1"><strong>The solution:</strong> </h2>



<p>When the client first contacted EstateGuru he already had a construction permit and ownership of the land plot. Construction works were already underway so the client was interested in fast decisions and didn’t want to spend months in negotiations and project analysis which are the usual practise with institutional creditors.&nbsp;</p>



<figure class="wp-block-image size-large is-resized"><a href="../wp-content/uploads/2020/05/Image-1.jpg" data-rel="lightbox-image-0" data-rl_title="" data-rl_caption="" title=""><img src="{{URL::to('')}}/public/assets/frontend/blog_assets/wp-content/uploads/2020/05/Image-1.jpg" alt class="wp-image-3367 jetpack-lazy-image" width="580" height="334" data-lazy-src="https://blog.estateguru.co/wp-content/uploads/2020/05/Image-1.jpg?is-pending-load=1" srcset="data:image/gif;base64,R0lGODlhAQABAIAAAAAAAP///yH5BAEAAAAALAAAAAABAAEAAAIBRAA7"><noscript><img  alt=""  width="580" height="334" data-src="https://blog.estateguru.co/wp-content/uploads/2020/05/Image-1.jpg" class="wp-image-3367 lazyload" src="data:image/gif;base64,R0lGODlhAQABAAAAACH5BAEKAAEALAAAAAABAAEAAAICTAEAOw==" /><noscript><img src="{{URL::to('')}}/public/assets/frontend/blog_assets/wp-content/uploads/2020/05/Image-1.jpg" alt="" class="wp-image-3367" width="580" height="334"/></noscript></noscript></a><figcaption>The building site being prepared for construction</figcaption></figure>



<p>The project is an attractive, ambitious building. The facade combines elements of Jurmala’s historical architecture from the beginning of the 20th century like carved elements and turrets with a modern approach. Interiors are great examples of ergonomic modern living space featuring a combination of glass, wood, and stone.</p>



<p>EstateGuru came up with a prompt solution and by May 2018 the first project was on the platform and the opening loan’s money transfer hit the client’s account.</p>



<p>Investors were pretty active and enthusiastic because from a risk point of view the project was attractive with a first stage LTV of 29% (projected LTV 65%). The loan was backed by a first-rank mortgage and personal surety from the management board in the full mortgage amount.</p>



<figure class="wp-block-image size-large is-resized"><a href="../wp-content/uploads/2020/05/Image-2.jpg" data-rel="lightbox-image-1" data-rl_title="" data-rl_caption="" title=""><img src="{{URL::to('')}}/public/assets/frontend/blog_assets/wp-content/uploads/2020/05/Image-2.jpg" alt class="wp-image-3368 jetpack-lazy-image" width="573" height="349" data-lazy-src="https://blog.estateguru.co/wp-content/uploads/2020/05/Image-2.jpg?is-pending-load=1" srcset="data:image/gif;base64,R0lGODlhAQABAIAAAAAAAP///yH5BAEAAAAALAAAAAABAAEAAAIBRAA7"><noscript><img  alt=""  width="573" height="349" data-src="https://blog.estateguru.co/wp-content/uploads/2020/05/Image-2.jpg" class="wp-image-3368 lazyload" src="data:image/gif;base64,R0lGODlhAQABAAAAACH5BAEKAAEALAAAAAABAAEAAAICTAEAOw==" /><noscript><img src="{{URL::to('')}}/public/assets/frontend/blog_assets/wp-content/uploads/2020/05/Image-2.jpg" alt="" class="wp-image-3368" width="573" height="349"/></noscript></noscript></a><figcaption>The building during construction</figcaption></figure>



<p>The developer&#8217;s business plan was also appropriate: planned apartment areas started from a compact 46 sqm, priced from 2500 EUR per sqm, which make apartments affordable not only for foreign and wealthy customers but also for the local buyers. In addition, the project is attractive as an investment property, because during summer season apartments in Jurmala are in high demand as holiday properties.</p>



<p>At the moment the project is in its final stage with interior works planned in July 2020. Five out of the eighteen apartments have already found buyers. So in the coming months, EstateGuru investor’s funds will return to the platform and new residents will receive keys for their brand new living place.</p>



<figure class="wp-block-image size-large is-resized"><a href="../wp-content/uploads/2020/05/Image-3-new.jpg" data-rel="lightbox-image-2" data-rl_title="" data-rl_caption="" title=""><img src="{{URL::to('')}}/public/assets/frontend/blog_assets/wp-content/uploads/2020/05/Image-3-new.jpg" alt class="wp-image-3369 jetpack-lazy-image" width="586" height="317" data-lazy-src="https://blog.estateguru.co/wp-content/uploads/2020/05/Image-3-new.jpg?is-pending-load=1" srcset="data:image/gif;base64,R0lGODlhAQABAIAAAAAAAP///yH5BAEAAAAALAAAAAABAAEAAAIBRAA7"><noscript><img  alt=""  width="586" height="317" data-src="https://blog.estateguru.co/wp-content/uploads/2020/05/Image-3-new.jpg" class="wp-image-3369 lazyload" src="data:image/gif;base64,R0lGODlhAQABAAAAACH5BAEKAAEALAAAAAABAAEAAAICTAEAOw==" /><noscript><img src="{{URL::to('')}}/public/assets/frontend/blog_assets/wp-content/uploads/2020/05/Image-3-new.jpg" alt="" class="wp-image-3369" width="586" height="317"/></noscript></noscript></a><figcaption>The final touches being added</figcaption></figure>



<p><strong>Note:</strong> An interesting coincidence occurred with this project when we found that both the borrower’s Operating Manager and EstateGuru’s loan manager had almost exactly the same name and surname, with just one small difference. This is purely coincidental, and they are not related in any way.</p>



<p><strong>Why did you choose EstateGuru to finance this project?</strong></p>



<p><em>“Unfortunately, banks have extremely bureaucratic procedures, as banks are more formal in their structure and philosophy. In this situation, EstateGuru has the advantage of making decisions and evaluating projects much faster” &#8211; </em><strong>Artūrs Tarvīds</strong><strong>. Operating manager &#8211; SIA Jomas 19</strong></p>



<p><strong>What was your experience of working with Arturs (EstateGuru loan manager)?&nbsp;</strong></p>



<p><em>“I consider Artūrs and all EstateGuru team as high-level professionals, who always process our financing or information requests very quickly. Fast project analysis and funding without bureaucracy – it is EstateGuru style” &#8211; </em><strong>Artūrs Tarvīds. Operating manager &#8211; SIA Jomas 19</strong></p>



<h2><strong>EstateGuru Feedback</strong></h2>



<p><em>“Artūrs is a responsible, proactive and open-minded person. It is always easy to communicate and to search for solutions if some issues appear” </em><strong>Artūrs Tarvids, EstateGuru Loan manager</strong></p>

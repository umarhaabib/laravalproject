<p>In May, the value of defaulted loans decreased to €5,5M. We recovered one loan (partial repayment) in Latvia and one similar recovery occurred at the beginning of June. Improving our debt collection has been the focus during the last few months and we are continuing with more aggressive sale of the collateral of defaulted loans.</p>




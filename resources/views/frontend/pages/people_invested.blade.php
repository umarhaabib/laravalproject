<p>EstateGuru is currently running a fundraising campaign on the Seedrs platform which allows investors to gain equity in the company.</p>



<p>The campaign has been a huge success thus far, hitting the minimum target while it was still in private mode and only available to existing EstateGuru investors. Now it is open to the public and there is still plenty of time to join us on our mission to change the future of property financing across Europe.</p>



<p>To understand what motivates people to invest and what they expect to gain from doing so, we spoke to three investors from different countries to get their stories.</p>



<span id="more-3165"></span>



<h2><strong>Wolfgang Schmidt</strong></h2>



<h3>Germany</h3>



<p><strong>How long have you been a regular investor on the EstateGuru platform?</strong></p>



<p>About roughly two years.</p>



<p><strong>How did your experience in using EstateGuru for property investments influence your decision to invest in equity on Seedrs?</strong></p>



<p>The EG business model with mortgage-backed loans is an interesting way of investing in the real estate-related asset class. The challenges in this loan business are obvious and EG handles the processes regarding delayed and defaulted loans in quite a good manner. Of course, as a relatively young company, they had to improve their processes and their communication to the loan investors over time. And they did it quite well. So it seems to me that it is reasonable to assume that they might be able to scale this business in Europe or elsewhere in the world. Additionally, their expansion to Finland seems to be in good shape. Since they were also able to start in a difficult market like Germany with an interesting first loan and how they handled issues that arose with this, it all was definitely building more and more trust in their capabilities.</p>



<p><strong>Is this your first time buying equity in a company? If yes, what convinced you to start now?</strong></p>



<p>No, I am a regular business angel and hold public shares too.</p>



<p><strong>How did you find the experience of using Seedrs to invest in EstateGuru?</strong></p>



<p>Using platforms like Seedrs is quite convenient.&nbsp; The ability to communicate not only with the fund-raising company but to discuss with the investor community too was very helpful. Itâ€™s the same experience I have with Companisto in Germany. Sure you pay for this with a high carry in case of success but this is worthwhile.&nbsp;</p>



<p><strong>How do you expect your EstateGuru investment to perform?</strong></p>



<p>Hopefully good of course ðŸ™‚ They should be able to scale their business all around the world and conquer the special niche they are working in, so this should result in a high multiple on exit.</p>



<p><strong>Any advice for people who are still considering an investment in this fundraising round?</strong></p>



<p>Generally speaking, if you consider investing a considerable amount in equity you should understand the business model. So read all the information like the pitch deck and disclosed numbers and get in touch with EG for all your questions via the Seedrs discussion board for the funding round.&nbsp;</p>



<p><strong>What would you like to see from EstateGuru now that you are a shareholder?</strong></p>



<p>Besides the above-mentioned scaling expectations, I hope for regular information about the ongoing business results like quarterly updates. Additionally, they should leverage the power of the investor crowd, because I assume there might be some knowledgeable market insiders in this group who could help in their business expansion to other countries.</p>




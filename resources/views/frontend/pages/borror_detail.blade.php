<p>When Feldberg OÜ needed some capital to develop a new apartment building, they turned to EstateGuru and we were able to assist in record time.</p>



<span id="more-3481"></span>



<p><strong>Client:</strong> Feldberg OÜ &#8211; A medium size construction company, founded in 2001.</p>



<p><strong>Amount raised:</strong> €760 000</p>



<p><strong>Purpose of loan:</strong> Developing an apartment building with 21 apartments in the City of Maardu, Harju County, Estonia.&nbsp;</p>



<p><strong>Loan period:</strong> 12 months</p>



<p><strong>Interest rate:</strong> 10,5% per annum</p>



<p><strong>Stages:</strong> 8 stages</p>



<p><strong>Actual interest rate:</strong> 4,28% per annum</p>



<p><strong>EstateGuru contract fee:</strong> 3%</p>



<p><strong>Total cost (actual):</strong> 7,28% per annum</p>



<p><strong>Repayment type:</strong> Full bullet&nbsp;</p>



<p><strong>Max LTV:</strong> 67.0%</p>




<h2>Timeline of the process:</h2>



<p><strong>Day 1:</strong> Valuation of property sent and request filled in</p>



<p><strong>Day 2: </strong>Positive decision from EstateGuru</p>



<p><strong>Day 3: </strong>Signing of the contract in EstateGuru portal, loan published for EstateGuru investors</p>



<p><strong>Day 4:</strong> Loan amount filled up, notary agreed.</p>



<p><strong>Day 5:</strong> Mortgage agreement signed in notary, loan paid out</p>



<figure class="wp-block-image size-large is-resized"><a href="../wp-content/uploads/2020/06/Ringi_Elutuba.jpg" data-rel="lightbox-image-1" data-rl_title="" data-rl_caption="" title=""><img src="{{URL::to('')}}/public/assets/frontend/blog_assets/wp-content/uploads/2020/06/Ringi_Elutuba.jpg" alt class="wp-image-3482 jetpack-lazy-image" width="592" height="333" data-lazy-src="https://blog.estateguru.co/wp-content/uploads/2020/06/Ringi_Elutuba.jpg?is-pending-load=1" srcset="data:image/gif;base64,R0lGODlhAQABAIAAAAAAAP///yH5BAEAAAAALAAAAAABAAEAAAIBRAA7"><noscript><img  alt=""  width="592" height="333" data-src="https://blog.estateguru.co/wp-content/uploads/2020/06/Ringi_Elutuba.jpg" class="wp-image-3482 lazyload" src="data:image/gif;base64,R0lGODlhAQABAAAAACH5BAEKAAEALAAAAAABAAEAAAICTAEAOw==" /><noscript><img src="{{URL::to('')}}/public/assets/frontend/blog_assets/wp-content/uploads/2020/06/Ringi_Elutuba.jpg" alt="" class="wp-image-3482" width="592" height="333"/></noscript></noscript></a></figure>



<p>Vadim Kilk, the owner of Feldberg OÜ was eager to develop a new apartment building in the area of Maardu, which lies on the outskirts of Estonian capital Tallinn. He places a high value on speed, efficiency and building a personal relationship with both his loan manager and the credit provider in general.</p>



<p>As he says: “I can start financial planning with my project manager at EstateGuru within a day and the knowledge and speed of action are excellent!”</p>



<p>When time is of the essence, the ability to simply pick up the phone and talk directly to the person managing your loan application rather than having to schedule an appointment or call days in advance becomes a major advantage.</p>



<p>Vadim also really appreciates the fact that he is able to speak to the same person every time, and that there is a real interest in helping him achieve success. In this case, that person is EstateGuru loan manager Tauri Tiik.</p>



<p>“The loan manager cares about how the project is doing, they visit the construction site and what’s most important &#8211; they are humans, not robots!” he says.</p>



<p>So does he plan to continue using EstateGuru for future developments?&nbsp;</p>



<p>“Yes, definitely. I see a long cooperation with both my loan manager and EstateGuru.”</p>



<p>As for EstateGuru’s loan manager Tauri, he says he really enjoys working with Vadim and his company: “It was a real honour working with the company. Everything was done smoothly and professionally, which makes my job a lot easier.”<br></p>



<p>EstateGuru and Vadim Kilk are already collaborating on a new project following the success of the previous one. This time it is an apartment building, again in the area of Maardu, where Feldberg OU is building 30 brand new apartments.&nbsp; You can see more details of this development on their web page: <a rel="noreferrer noopener" href="http://www.m7.ee/" target="_blank">www.m7.ee</a></p>

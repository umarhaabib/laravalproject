<p>Hi! My name is Antti and I am an investor.</p>



<p>I will not dwell long on the introductions. I’ll admit freely: I am religious. Not in the common sense of the word though. I believe in real estate. My investment portfolio consists of 100% real estate and I can say with conviction that I am a firm believer in the power of property.&nbsp;</p>



<span id="more-3113"></span>



<p>I do not relate to the concept of ‘ups and downs’, prevalent in the security markets: I just cannot grasp it. My way of thinking tells me that over the course of years, every drop in real estate prices will eventually end with a rebound. In the case of crypto securities, it has happened that the prices that go down never come back up again. It is true that there have been moments related to some extra innovative projects (e.g. Tesla) when I have thought about buying a few shares but these have remained just thoughts. Every investment means contributing towards an emotion, a dream, towards something you believe in. And, as I already said, I believe in real estate. Because it is there in bricks and mortar and you can physically feel what it means to own it.</p>



<p>My ‘affair’ with real estate started at the beginning of the century when I was at university and used my student loan to buy my very first apartment. Already back then, I understood that it was better to be an owner than a tenant. My enthusiasm for the property market grew when I managed to sell my second apartment at a 30% margin after having bought it six months earlier. My profitable deals and activity were spotted and I received an invitation to work in the investments team of Pindi real estate agency. This marked the beginning of a period when I did not buy much for myself but managed the money of investors instead.&nbsp;</p>



<p>In 2008, I moved on to valuation and brokering, which gave me an even wider and better feel of the market. During the post-2008 rebound, we made 50-60 investments in apartments a year, at least one deal per week. At some point, I discovered that all that hard work had left me with no time to spend the money earned, and I decided to start increasing my own investment portfolio. Currently, I am managing a passive portfolio of about two dozen units in Tallinn (including one commercial building), some of which have been purchased in partnership with other investors.</p>



<p>My main rule for managing my property portfolio is simple – the assets need to be sellable at any moment, both legally and physically. I have never acquired large or highly exclusive apartments. During times of crisis, they may become a burden and the income may suffer a lot. It is better to be wise and energetic.&nbsp;</p>



<p>I have often witnessed that in good times, people tend to forget that difficult times will inevitably follow. Decisions are made hastily, without analysing all the relevant aspects. My main job at EstateGuru is risk management. I am as committed to it as I am committed to my own portfolio and apply all my accumulated knowledge and experience to the valuation of EstateGuru’s securities and managing their sale.&nbsp;</p>



<p>So yes, most of my income comes from rent. As I am not that great at controlling my expenses or saving money, I have decided to focus more on controlling my income. Don’t be afraid of large expenses, beware of small income. Yes, tenants may sometimes be problematic, you need to listen to their worries and handle very different situations. But for stable high yield, I do this gladly. I have crisis experience, so even the anxious anticipation characterising the current situation is not really a major source of stress for me.</p>

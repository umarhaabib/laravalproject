		<h1 class="entry-title">Case Study &#8211; How we financed a record-breaking development in Saaremaa, Estonia</h1>	</header><!-- .entry-header -->

	
	
		<div class="post-thumbnail">
			<img width="1200" height="675" src="{{URL::to('')}}/public/assets/frontend/blog_assets/wp-content/uploads/2020/06/English.jpg" class="attachment-post-thumbnail size-post-thumbnail wp-post-image jetpack-lazy-image" alt="" data-lazy-sizes="(max-width: 709px) 85vw, (max-width: 909px) 67vw, (max-width: 984px) 60vw, (max-width: 1362px) 62vw, 840px" data-lazy-src="https://blog.estateguru.co/wp-content/uploads/2020/06/English.jpg?is-pending-load=1" srcset="data:image/gif;base64,R0lGODlhAQABAIAAAAAAAP///yH5BAEAAAAALAAAAAABAAEAAAIBRAA7" />	</div><!-- .post-thumbnail -->

	
	<div class="entry-content">
		
<p>EstateGuru is proud to be the recipient of the largest one-time loan repayment in the history of Estonia, after receiving €2 327 600 which was lent for the construction of the WOW Family Centre in Kuressaare, on the island of Saaremaa.</p>



<span id="more-3447"></span>



<h2><strong>The client:</strong> </h2>



<p>Tulik Invest OÜ is an investment company specializing in real estate development, offering management services and business consultations.</p>



<h2><strong>The development: </strong></h2>



<p>An ambitious mixed-use facility that is expected to attract 80 000 visitors during its first year after opening, the WOW Family Centre will feature a sports club, a massive grocery store, and a large family entertainment area. The fitness club opened in June 2020, with one of the biggest retailers in Estonia, Selver, set to open their store on July 9.&nbsp;</p>



<figure class="wp-block-image size-large is-resized"><a href="{{URL::to('')}}/public/assets/frontend/blog_assets/wp-content/uploads/2020/06/before.png" data-rel="lightbox-image-0" data-rl_title="" data-rl_caption="" title=""><img src="{{URL::to('')}}/public/assets/frontend/blog_assets/wp-content/uploads/2020/06/before.png" alt class="wp-image-3448 jetpack-lazy-image" width="587" height="330" data-lazy-src="https://blog.estateguru.co/wp-content/uploads/2020/06/before.png?is-pending-load=1" srcset="data:image/gif;base64,R0lGODlhAQABAIAAAAAAAP///yH5BAEAAAAALAAAAAABAAEAAAIBRAA7"><noscript><img  alt=""  width="587" height="330" data-src="https://blog.estateguru.co/wp-content/uploads/2020/06/before.png" class="wp-image-3448 lazyload" src="data:image/gif;base64,R0lGODlhAQABAAAAACH5BAEKAAEALAAAAAABAAEAAAICTAEAOw==" /><noscript><img src="{{URL::to('')}}/public/assets/frontend/blog_assets/wp-content/uploads/2020/06/before.png" alt="" class="wp-image-3448" width="587" height="330"/></noscript></noscript></a></figure>



<h2><strong>The request: </strong></h2>



<p>Despite the prime location in one of Estonia’s favourite tourist destinations, Tulik Invest could not arrange financing through a bank, as they uniformly felt that giving a construction loan on a project this highly ambitious was simply too risky.&nbsp;</p>



<h2><strong>The solution:</strong> </h2>



<p>Tulik Invest approached EstateGuru to finance the construction of the project in stages. After careful consideration and some serious planning, we agreed to finance the construction of the centre in 13 stages. The first stage of the project was released on the EstateGuru platform on 15 April 2019 and was quickly funded thanks to the backing of 730 investors.</p>



<p>In total, the 13 stages attracted 12,952 individual investments which is a testament to the appetite of EstateGuru’s investors for large projects.</p>



<p>This represents one of the biggest stage projects funded in the history of Estonia.&nbsp;</p>



<figure class="wp-block-image size-large is-resized"><a href="../wp-content/uploads/2020/06/after.jpg" data-rel="lightbox-image-1" data-rl_title="" data-rl_caption="" title=""><img src="{{URL::to('')}}/public/assets/frontend/blog_assets/wp-content/uploads/2020/06/after.jpg" alt class="wp-image-3449 jetpack-lazy-image" width="592" height="444" data-lazy-src="https://blog.estateguru.co/wp-content/uploads/2020/06/after.jpg?is-pending-load=1" srcset="data:image/gif;base64,R0lGODlhAQABAIAAAAAAAP///yH5BAEAAAAALAAAAAABAAEAAAIBRAA7"><noscript><img  alt=""  width="592" height="444" data-src="https://blog.estateguru.co/wp-content/uploads/2020/06/after.jpg" class="wp-image-3449 lazyload" src="data:image/gif;base64,R0lGODlhAQABAAAAACH5BAEKAAEALAAAAAABAAEAAAICTAEAOw==" /><noscript><img src="{{URL::to('')}}/public/assets/frontend/blog_assets/wp-content/uploads/2020/06/after.jpg" alt="" class="wp-image-3449" width="592" height="444"/></noscript></noscript></a></figure>



<h2><strong>The result:</strong> </h2>



<p>Despite the island of Saaremaa going into a complete shutdown midway through construction due to being at the centre of the largest Estonian outbreak of the Covid-19 virus, lease agreements with the sports club 24-7 Fitnessklubi OÜ and the grocery store Selver AS remained in force.</p>



<p>By early June 2020, construction had progressed sufficiently for 24-7 Fitnessklubi to take up their tenancy.&nbsp;</p>



<p>With construction all but complete, the borrower was able to secure refinancing on the loan and return an astonishing €2 327 600 to EstateGuru investors on June 18, 2020.&nbsp;</p>



<p>As the biggest single repayment in Estonian history, the successful completion of the deal represents a significant milestone in our history and shows that our investors have the appetite to match any borrower’s ambitions, no matter how big. We are happy to have contributed to the development of local communities.</p>

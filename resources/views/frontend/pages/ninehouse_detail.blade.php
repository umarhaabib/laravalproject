<p><strong>The client:</strong> Apa Buvnieks is a regional house-building company with 10+ years of experience in this sector.</p>



<p><strong>The development:</strong> The plot had development permission for nine new-build houses with a mixture of three, four-bed units.</p>



<span id="more-3394"></span>



<figure class="wp-block-image size-large is-resized"><a href="../wp-content/uploads/2020/06/Sample3.jpg" data-rel="lightbox-image-0" data-rl_title="" data-rl_caption="" title=""><img src="{{URL::to('')}}/public/assets/frontend/blog_assets/wp-content/uploads/2020/06/Sample3.jpg" alt class="wp-image-3395 jetpack-lazy-image" width="585" height="329" data-lazy-src="https://blog.estateguru.co/wp-content/uploads/2020/06/Sample3.jpg?is-pending-load=1" srcset="data:image/gif;base64,R0lGODlhAQABAIAAAAAAAP///yH5BAEAAAAALAAAAAABAAEAAAIBRAA7"><noscript><img  alt=""  width="585" height="329" data-src="https://blog.estateguru.co/wp-content/uploads/2020/06/Sample3.jpg" class="wp-image-3395 lazyload" src="data:image/gif;base64,R0lGODlhAQABAAAAACH5BAEKAAEALAAAAAABAAEAAAICTAEAOw==" /><noscript><img src="{{URL::to('')}}/public/assets/frontend/blog_assets/wp-content/uploads/2020/06/Sample3.jpg" alt="" class="wp-image-3395" width="585" height="329"/></noscript></noscript></a><figcaption>A concept drawing for the development</figcaption></figure>



<h2><strong>The request:</strong></h2>



<p>1.&nbsp; The borrower already owned the land plot and had created detailed planning for nine houses in Salaspils, Latvia.</p>



<p>2.&nbsp; A construction loan of €410 000 over 14 months for the total construction costs in order to build and then to sell the nine houses.</p>



<p>3.&nbsp; Quick loan issuance in stages.</p>



<p>By the time the land was purchased, the borrower knew that he could not build nine houses without credit money. Options began to be calculated in advance so as not to drown in loans and not to be left only with the land plot.</p>



<p>The borrower has never used alternative financing before but now feels that discovering EstateGuru was his “golden ticket”. All the borrower’s previous construction-developments projects were completed with traditional funding from banks.</p>



<figure class="wp-block-image size-large"><a href="../wp-content/uploads/2020/06/IMG_0188.jpg" data-rel="lightbox-image-1" data-rl_title="" data-rl_caption="" title=""><img src="{{URL::to('')}}/public/assets/frontend/blog_assets/wp-content/uploads/2020/06/IMG_0188.jpg" alt class="wp-image-3396 jetpack-lazy-image" data-lazy-src="https://blog.estateguru.co/wp-content/uploads/2020/06/IMG_0188.jpg?is-pending-load=1" srcset="data:image/gif;base64,R0lGODlhAQABAIAAAAAAAP///yH5BAEAAAAALAAAAAABAAEAAAIBRAA7"><noscript><img  alt=""  data-src="https://blog.estateguru.co/wp-content/uploads/2020/06/IMG_0188.jpg" class="wp-image-3396 lazyload" src="data:image/gif;base64,R0lGODlhAQABAAAAACH5BAEKAAEALAAAAAABAAEAAAICTAEAOw==" /><noscript><img src="{{URL::to('')}}/public/assets/frontend/blog_assets/wp-content/uploads/2020/06/IMG_0188.jpg" alt="" class="wp-image-3396"/></noscript></noscript></a><figcaption>One of the completed houses</figcaption></figure>



<p>The experience of the borrower shows that construction loans are rarely approved by banks. Even after approval, construction documents may not pass verification. It means that the loan will be refused. This is an internal regulation of the bank and it cannot be changed.</p>



<p>The funding was agreed and delivered on all of his projects. He was very positive about the speed and flexibility of the EstateGuru team.</p>



<p>At the moment, nine houses have been built with a total area of ​​about 150m2 each with 600m2 of adjoining territory. In almost all houses, new residents have already been settled.</p>



<p>Apa Buvnieks owner Pavels Fedotovs<strong> </strong>says he is happy that the new residents exchanged their cramped apartments for spacious and high-quality houses in nature.</p>



<p>The introduction to Estateguru was successful and the client will prepare a new development project with our help soon.</p>



<p><strong>Why did you choose EstateGuru to finance this project?</strong></p>



<p>“We chose this company because already during the period of consideration of our application, the attitude of the company&#8217;s employees and the deepening of our problem confirmed our right choice. We were offered several loan options, taking into account the loan amounts, interest, terms, repayment conditions. We received the best offer &#8211; a loan with flexible terms. It is also important that the loan process is digitized. We especially appreciate the favourable attitude of the company&#8217;s employees, their approach and especially the insight into the nuances of our specific situation, which we encountered already at the application stage and which has justified the choice of EstateGuru Latvia as our strategic partner<strong>.</strong>” &#8211; <strong>Pavels Fedotovs &#8211; APA Buvnieks owner</strong></p>



<p><strong>What was your experience of working with Maksims? (EstateGuru loan manager)?&nbsp;</strong>“We would like to express our gratitude to EstateGuru Latvia credit manager Maksims for his honest attitude towards his work, providing us with consultations.” &#8211; <strong>Pavels Fedotovs &#8211; APA Buvnieks owner</strong></p>

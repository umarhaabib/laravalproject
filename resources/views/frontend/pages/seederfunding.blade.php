<p>EstateGuru’s equity fundraising campaign on Seedrs has been open to all investors since 4 May, and the results have been beyond our wildest expectations.</p>



<p>We set ourselves a minimum funding target of €350 000 before the campaign started, and this target was reached even before the campaign officially opened and while only our existing investors were invited to purchase shares.</p>



<span id="more-3274"></span>



<p>Since opening it to the Seedrs community and the wider public, we have been backed by 815 investors who have helped us get to reach 184% overfunded as of 22 May. This amounts to in excess of €642 000 worth of investment. The upper limit for this campaign is €2 million and it is still open until June 6th.</p>



<p>You can see the campaign <a href="https://www.seedrs.com/estateguru/" target="_blank" rel="noreferrer noopener"><strong>here</strong></a><strong>.</strong></p>



<p>We would like to extend a heartfelt thanks to everyone who has invested so far. Your faith and trust in EstateGuru, our team, and our business model are extremely gratifying. We will make it our mission to reward this trust.</p>



<p>If you&#8217;d like an update on the campaign in video format, you can watch <strong><a href="https://www.youtube.com/watch?v=88740ZhxCjA" data-rel="lightbox-video-0" target="_blank" rel="noreferrer noopener">this recording</a></strong> of our recent webinar, where our senior management answered investor questions on the funding round.</p>



<h2><strong>What does equity fundraising mean?</strong></h2>



<p>The campaign allows users of EstateGuru and Seedrs to purchase shares in our company. What this means is that those who invest will become shareholders in EstateGuru. If the company decides to float on a stock exchange or pay dividends to shareholders in future, those who invested will gain financial benefits according to the number of shares they hold. The Seedrs investors will be represented as a collective by Seedrs at the EstateGuru board level. In addition, the shares can be sold on the Seedrs secondary market at any time in the future. Details of how EstateGuru plans to use this investment follow later in this blog.</p>



<h2><strong>Key questions</strong></h2>



<p>Since launching the campaign we have received many questions from investors about EstateGuru’s valuation and our roadmap for the future. We thought it would be good to use this opportunity to share the answers with our broader investment community as well.</p>



<p><strong>Valuation</strong>&nbsp;</p>



<p>Many investors were curious about the company valuation of €28,824,215 and how that amount was determined. In essence, the valuation of fintech and lending companies like EstateGuru is defined by many factors. Some of them are measurable in a better manner than others. It also depends on the company phase. EstateGuru is clearly in the growth phase as this campaign is part of our series A raise.</p>



<p>Over the past 6 years, EstateGuru has built a sophisticated tech platform and a business system that enable it to grow aggressively throughout the next years to achieve its goals. The company has already entered three new markets and has made preparations and first deals to enter four more in the next 12 months. We have a strong and motivated team of 44 professionals from different fields in place to achieve these goals. We have more than two years of successful track record working with a German bank making it much easier to establish new relationships with new ones.</p>



<p>Here are some metrics used to value fintech and similar companies in our space. This is how we determined our current company value:</p>



<p><strong>Outstanding loan volume.</strong> EstateGuru’s current outstanding loan volume is €103 million. According to our research valuation multipliers considering this metric are between 0,4-1,2 x. That makes the valuation range €41-€123 million.</p>



<p><strong>Last 12 months of revenue (used mostly for M&amp;A transactions)</strong>. EstateGuru’s revenue from March 2019 &#8211; March 2020 was €3.065 million. Standard multipliers including this metric for establishing a valuation of fintech and lending platforms are between 6-30 x revenue. That makes valuation range €18.4-92 million.</p>



<p><strong>Last 12 months of lending volume.</strong> EstateGuru’s lending volume from March 2019 &#8211; March 2020 was €94.279 million. Standard multipliers including this metric for establishing a valuation of lending platforms have been between 0.5-2 x of the last years lending volume. That makes valuation range €47-188 million.</p>



<h2><strong>Our plans for the future</strong></h2>



<p>Another question we have received many times is what exactly we plan to do with this investment. The money raised will be used for three strategic areas:</p>



<ol><li><strong>Geographical expansion.</strong>&nbsp;</li></ol>



<p>We have already gained a foothold in both the German and Finnish markets, and we aim to aggressively expand in both of these in the future. In addition, we will look to scale to even more European countries. We believe this will be of great benefit to our investors who will gain a wider range of loans to build their portfolio, and also help SMEs in these countries gain access to fast, reliable capital.&nbsp;</p>



<ol start="2"><li><strong>Delivering an ambitious technology roadmap</strong>&nbsp;</li></ol>



<p>We plan to onboard additional engineering and product management resources, execute a data road map, advance partnerships and integrations with third-party service providers, further our digitalization and investment automation, and make investments in EstateGuru’s tech-infrastructure.</p>



<ol start="3"><li><strong>Developing the area of capital markets and institutional investments.</strong>&nbsp;</li></ol>



<p>We are looking to hire a capital markets team, conduct the legal set-up of new products and contracts, develop institutional investor networks, develop instant data sharing (reporting environment &amp; API integration), and an online interface for institutional partners</p>



<p>EstateGuru aims to develop and grow its product and business to become the biggest real estate financing platform in Europe by 2025. The specifics of where the raised funds will be allocated depends on the final raised amount. Our approach has always been to be an agile company that is able to react promptly to new market opportunities and challenges while maintaining a clear roadmap for our investors. While having developed a clear product roadmap with several completely new revenue streams and opportunities for investors, it is clear that announcing these ideas would inevitably mean us losing our competitive edge and strategic advantage over our competitors. This includes both improving our existing features (Auto Invest, secondary market…), and creating new features and, of course, new products.&nbsp;</p>



<p>We hope you will be with us on every step of the way during this exciting journey. If you have not yet participated in the round you still have time to do so &#8211; if you have any questions, feel free to contact us via <a rel="noreferrer noopener" href="mailto:info@estateguru.co" target="_blank">info@estateguru.co</a> and we will assist you in the process.</p>



